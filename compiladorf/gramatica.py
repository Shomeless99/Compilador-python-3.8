lista_filtrar = ["PROGRAMA", "SENTENCIAS"]

def reemplazar_claves(gramatica, claves_reemplazo):
    reemplazado = []
    claves = list(map(lambda x: x[0], claves_reemplazo))
    for objeto in gramatica:
        nombre = list(objeto.keys())[0]
        if(nombre in claves):
            reemplazado.append({claves_reemplazo[claves.index(nombre)][1]:objeto[nombre]})
        else:
            reemplazado.append(objeto)
    return reemplazado
def filtrar(gramatica,claves):
    filtrado = []
    for objeto in gramatica:
        nombre = list(objeto.keys())[0]
        if(nombre not in claves):filtrado.append(objeto)
    return filtrado
gramatica_individual = filtrar(gramatica, lista_filtrar)


